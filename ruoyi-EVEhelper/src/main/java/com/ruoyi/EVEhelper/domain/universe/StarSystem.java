package com.ruoyi.EVEhelper.domain.universe;

public class StarSystem {
    private String constellation_id;
    private String name;
    private String planets;
    private String position;
    private String security_class;
    private String security_status;
    private String star_id;
    private String stargates;
    private String stations;
    private String system_id;

    public String getConstellation_id() {
        return constellation_id;
    }

    public void setConstellation_id(String constellation_id) {
        this.constellation_id = constellation_id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPlanets() {
        return planets;
    }

    public void setPlanets(String planets) {
        this.planets = planets;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getSecurity_class() {
        return security_class;
    }

    public void setSecurity_class(String security_class) {
        this.security_class = security_class;
    }

    public String getSecurity_status() {
        return security_status;
    }

    public void setSecurity_status(String security_status) {
        this.security_status = security_status;
    }

    public String getStar_id() {
        return star_id;
    }

    public void setStar_id(String star_id) {
        this.star_id = star_id;
    }

    public String getStarGates() {
        return stargates;
    }

    public void setStarGates(String starGates) {
        this.stargates = starGates;
    }

    public String getStations() {
        return stations;
    }

    public void setStations(String stations) {
        this.stations = stations;
    }

    public String getSystem_id() {
        return system_id;
    }

    public void setSystem_id(String system_id) {
        this.system_id = system_id;
    }
}
